#!/bin/bash

function compile_all() {
  for f in *.c; do
    echo "compiling $f"
    gcc $f -o ${f%.*}
  done
}

function run() {
  compile_all
  # echo "============================================================================================================"
  # echo "exercise 1"
  # echo "============================================================================================================"
  # ./exercise1 < ex1.txt
  echo "============================================================================================================"
  echo "exercise 2"
  echo "============================================================================================================"
  ./exercise2
}

run
run > ./run_script_output.txt
